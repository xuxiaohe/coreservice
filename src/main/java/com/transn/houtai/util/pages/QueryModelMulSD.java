package com.transn.houtai.util.pages;

import java.io.Serializable;

/**
 * 
* @ClassName: QueryModelMulSD
* @Description:用于构建一个分页查询条件 包含多字段不同升降序排序
* @author tangli
* @date 2015年4月23日 下午2:30:34
*
 */
public class QueryModelMulSD implements Serializable {
private static final long serialVersionUID = 1L;
	private int n = 0;//第几页
	private int s = 10;//每页的记录数
	private String sd="ctime:DESC"; //多排序字段 参数格式为：ctime:DESC,utime:ASC
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public int getS() {
		return s;
	}
	public void setS(int s) {
		this.s = s;
	}
	public String getSd() {
		return sd;
	}
	public void setSd(String sd) {
		this.sd = sd;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	

}
